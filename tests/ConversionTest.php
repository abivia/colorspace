    public function testConversionNameToHex()
    {
        foreach (self::$samples as $name => $expect) {
            $color = Color::fromCss($name);
            $this->assertEquals($expect['hex'], $color->toCssHex(), $name);
        }
    }

    public function testConversionNameToHsb()
    {
        foreach (self::$samples as $name => $expect) {
            if ($expect['hsb'] === '') {
                continue;
            }
            $color = Color::fromCss($name);
            $hsb = new Hsb($color);
            $this->assertEquals($expect['hsb'], $hsb->toString(precision: 0), $name);
        }
    }

    public function testConversionNameToHsl()
    {
        foreach (self::$samples as $name => $expect) {
            $color = Color::fromCss($name);
            $hsl = new Hsl($color);
            $this->assertEquals($expect['hsl'], $hsl->toCss(legacy: true, precision: 0), $name);
        }
    }

    public function testConversionNameToRgb()
    {
        foreach (self::$samples as $name => $expect) {
            $color = Color::fromCss($name);
            $this->assertEquals($expect['rgb'], $color->toCss(true), $name);
        }
    }

